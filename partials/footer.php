        $("#nl-button").on('click', function() {
            var val = $('#nl-input').val()
            if (val) {
                console.log(val)
                var formData = new URLSearchParams();
                formData.append('email', val);

                const requestOptions = {
                    method: 'POST',
                    body: formData
                };
            

                $(".nl-alert").addClass('d-none');
                fetch('/send-newsletter.php', requestOptions)
                    .then(response => {
                        if (response.ok) {
                            // response.text().then(data => console.log(data))
                            $(".nl-alert.alert-success").removeClass('d-none');
                        } else {
                            // response.text().then(data => console.log(data))
                            $(".nl-alert.alert-danger").removeClass('d-none');
                        }
                    }).catch(error => {
                        // console.log(error)
                        $(".nl-alert.alert-danger").removeClass('d-none');
                    });
            }
        })
    })
